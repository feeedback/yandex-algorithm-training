import { test } from 'node:test';
import assert from 'node:assert/strict';;
import fn from './A.js';

test('1', () => {
  assert.strictEqual(
    fn([
      '7',
      '1 5',
      '10 -5',
      '1 10',
      '4 -2',
      '4 3',
      '4 1',
      '4 0', //
    ])
    , [
      '1 15',
      '4 2',
      '10 -5', //
    ]);
});

test('2', () => {
  assert.strictEqual(
    fn([
      '5',
      '5 -10000',
      '-5 100000000000',
      '10 2000000000000',
      '-5 -300000000000',
      '0 10000000000000', //
    ])
    , [
      '-5 -200000000000',
      '0 10000000000000',
      '5 -10000',
      '10 2000000000000', //
    ]);
});
